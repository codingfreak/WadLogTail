namespace codingfreaks.WadLogTail.Ui.WindowsApp.Enumerations
{
    /// <summary>
    /// Defines the type for a single window.
    /// </summary>
    public enum WindowType
    {
        /// <summary>
        /// Invalid window type.
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// Shows the main window.
        /// </summary>
        MainView = 1,

        /// <summary>
        /// Shows the selector tool window for the storage account.
        /// </summary>
        SelectStorageAccountView = 2
        
    }
}